# Operators in python

# Additional Operator
print(17+4) 

# Subtraction Operator
print(17-4)

# Multiplication Operator
print(17*4)

# Division Operator
print(17/4)

# Floor Division Operator
print(17//4)   # Only gives integer part of the value

# Modulus Operator
print(17%4)    # Gives the reminder

# Exponential Operator
print(17**4)    # Gives the power