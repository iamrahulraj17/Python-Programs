# Set is unorderd collection of data and the values is not repeated in set it can be created by { }

s = {1,4,5,3,2,4,5} # int set
print(s)
print(type(s))

p = {4,"hello",False,6.847,4.0} # set of all data type
print(p)
print(type(p))

e = {} # it is not empty set it is dictionary
print(e)
print(type(e))

h = set() # it is empty set
print(h)
print(type(h))